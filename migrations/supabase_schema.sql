-- Create Tables
create table jobs (
  id bigint generated by default as identity primary key,
  company text not null,
  title text not null,
  location text,
  contract_type text,
  salary text,
  source text,
  compatibility int,
  description text,
  skills jsonb,
  posted_date date,
  remote boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table applications (
  id bigint generated by default as identity primary key,
  job_id bigint references jobs(id) on delete cascade,
  user_id uuid references auth.users(id), -- Link to Supabase Auth User
  status text check (status in ('draft', 'pending', 'sent', 'responded', 'to_modify')),
  created_date date default current_date,
  sent_date date,
  response_date date,
  cover_letter text,
  custom_email text,
  notes text,
  cv_path text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table recruiters (
  id bigint generated by default as identity primary key,
  name text,
  email text,
  linkedin text,
  company text,
  position text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- RLS Policies (Row Level Security)
alter table jobs enable row level security;
alter table applications enable row level security;
alter table recruiters enable row level security;

-- Policies
-- Jobs: Public read, Admin write (simplified to Public read for now)
create policy "Public jobs are viewable by everyone" on jobs for select using (true);

-- Applications: Users can CRUD their own applications
create policy "Users can view own applications" on applications for select using (auth.uid() = user_id);
create policy "Users can insert own applications" on applications for insert with check (auth.uid() = user_id);
create policy "Users can update own applications" on applications for update using (auth.uid() = user_id);
create policy "Users can delete own applications" on applications for delete using (auth.uid() = user_id);

-- Storage (Bucket: 'cvs')
insert into storage.buckets (id, name) values ('cvs', 'cvs');
create policy "CVs are publicly accessible" on storage.objects for select using (bucket_id = 'cvs');
create policy "Users can upload CVs" on storage.objects for insert with check (bucket_id = 'cvs' and auth.role() = 'authenticated');
